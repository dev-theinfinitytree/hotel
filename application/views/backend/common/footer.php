function updateStatus(value,id){
	$.ajax({
	   type: "POST",
	   url: "<?php echo base_url();?>dashboard/hotel_update_status",
	   data: "value="+value+"&id="+id,
	   success: function(msg){
		    $("#status_msg").html(msg);
			$("#status_msg").show();
			$("#status_msg").fadeOut(5000);
	   }
	});
}
